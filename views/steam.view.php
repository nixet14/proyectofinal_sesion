					<h3>¿Qué es Steam?</h3>
					<p>Steam es una plataforma de <strong>juegos digitales de PC</strong>&nbsp;de <strong>Valve</strong> Corporation, en funcionamiento desde 2003, y que ha conseguido posicionarse en el mercado como un estándar en la industria de videojuegos. En Steam puedes acceder a una biblioteca de <a href="https://juegosadn.eleconomista.es/estos-son-los-100-juegos-mas-vendidos-en-steam-durante-2016-no-102066/" target="_blank">más de 12.000 juegos</a> (datos de enero de 2017) que puedes comprar en Steam Store asociándolos a tu cuenta personal, pudiendo descargarlos, instalarlos y jugarlos en cualquier PC, mientras cumpla los requerimientos mínimos de cada juego. Steam es la <strong>plataforma líder de distribución digital de juegos</strong> de PC; recientemente Valve ha desvelado que la utilizan nada menos que <a href="https://juegosadn.eleconomista.es/steam-plataforma-digital-valve-pc-no-106534/" target="_blank">33 millones de jugadores al día</a> y que cuenta con 67 millones de usuarios activos al mes, unas cifras apabullantes. Steam es un entorno ideal para que los jugadores de PC puedan almacenar y coleccionar una inmensa biblioteca de juegos digitales, además de convivir con una comunidad (Steam Community) de más de 65 millones de gamers tan apasionados por los videojuegos como ellos.&nbsp;</p>


