					<h3>¿Qué servicios ofrece Steam?</h3>

					<p>Steam ofrece un amplio abanico de servicios totalmente gratuitos para el juego en PC (Windows/Linux) y Mac que diferencian a esta plataforma enormenente del juego en consola. Es posible acceder a ellas al <strong>descargar&nbsp;Steam</strong>&nbsp;(el cliente) y dependiendo de las características de cada juego.</p>

					<p><strong>1. Jugar a los juegos de tu cuenta en cualquier PC</strong></p>

					<p>Los juegos que compres&nbsp;<strong>se ligan a tu cuenta de Steam</strong>, quedando registrados para siempre a tu nombre, por lo que puedes instalarlos y desintalarlos todas las veces que quieras. Además puedes jugarlos en cualquier PC; tan sólo necesitas tener instalado el cliente de Steam, hacer log in con tu cuenta y descargar los juegos que quieras, como Dead Rising, Half-Life, Counter-Strike o cualquier otro.</p>

					<p><strong>2. Juego online gratuito</strong></p>

					<p>Steam ofrece juego online gratuito en PC&nbsp;<strong>sin ningún tipo de suscripción de pago</strong>&nbsp;de por medio, al contrario de lo que ocurre con el juego online en consola, que sí requiere de pago. La única excepción en Steam son algunos juegos MMO (juego en mundo persistente) como 'Final Fantasy XIV Online', ya que para acceder a sus servidores sí hay que pagar una suscripción mensual a la editora del juego (en ningún caso a Steam o Valve).</p>

					<p><strong>3. Actualizaciones automáticas</strong></p>

					<p>Los juegos del catálogo de Steam reciben&nbsp;<strong>actualizaciones constantes</strong>&nbsp;a juicio de la editora de cada videojuego, pero los jugadores no nos tenemos que preocupar en descargarlas, ya que Steam se encarga de mantener todos nuestros juegos actualizados a la última versión si lo queremos. El cliente de Steam también de actualiza frecuentemente de manera automática cuando abrimos cada sesión nueva.</p>

					<p><strong>4. Descargas de juegos ultrarrápidas</strong></p>

					<p>El ancho de banda para descargar juegos y actualizaciones en Steam es realmente alto, lo que se traduce en&nbsp;<strong>descargas muy rápidas</strong>&nbsp;para que podamos comenzar a jugar muy pocos minutos después de comprar nuestro juego en Steam Store (dependiendo de nuestra conexión, lógicamente).&nbsp;</p>

					<p><strong>5. Steam Workshop: mods para juegos de PC</strong></p>

					<p>Cada vez más y más juegos de Steam son compatibles con Steam Workshop, un directorio dentro de la plataforma donde los jugadores pueden&nbsp;<strong>descargar y compartir mods</strong>&nbsp;(modificaciones de todo tipo hechas por fans) para sus títulos favoritos. Desde mods meramente estéticos hasta otros que modifican completamente los juegos que toman como base.</p>

					<p><strong>6. Steam Cloud: Partidas guardadas en la nube</strong></p>

					<p><strong>&nbsp;</strong>Steam proporciona en muchos de sus títulos la posibilidad de&nbsp;<strong>guardar sus partidas en la nube</strong>&nbsp;además de en su PC de manera local. Esto significa que no hay que preocuparse de si cambiamos de ordenador o desinstalamos un juego, ya que en muchos casos, nuestras partidas guardadas se asocian a nuestra cuenta, pudiendo retomarlas en cualquier otro momento.</p>

					<p><strong>7. Steam Guard: mantén tu cuenta segura</strong></p>

					<p>Steam proporciona un&nbsp;<strong>servicio de seguridad&nbsp;</strong>para nuestras cuentas a través de su app móvil, de tal manera que si hacemos una compra o accedemos a nuestra cuenta en un ordenador nuevo, tendremos la posibilidad de verificar esta acción a través de nuestro smartphone. Este servicio evita un gran número de robos de cuentas por parte de terceros y es totalmente opcional.&nbsp;</p>

					<p><strong>8. Steam OS: su propio sistema operativo</strong></p>

					<p>Valve pone a disposición del jugador un<strong>&nbsp;sistema operativo abierto basado en Linux</strong>&nbsp;llamado Steam OS, que se puede descargar de forma totalmente gratuita, que parte de Debian 8 y que está totalmente enfocado para su uso en el salón.</p>

					<p><strong>9. Modo Big Picture: juega con mando en PC</strong></p>

					<p>Aquellos jugadores que quieran jugar exclusivamente con mando, pueden activar&nbsp;<strong>un interfaz alternativo</strong>&nbsp;que no requiere de ratón llamado Big Picture. Este modo nos permite desplazarnos por el interfaz de Steam usando únicamente un mando de control y se pueda activar/desactivar en cualquier momento.</p>

					<p><strong>10. Steam Play: juega donde quieras</strong></p>

					<p>Muchos juegos del catálogo de Steam incluyen la etiqueta 'Steam Play', lo que significa que puedes comprar ese juego em Steam Store y disfrutarlo tanto en&nbsp;<strong>PC&nbsp;</strong>(Windows y/o Linux) como en&nbsp;<strong>ordenadores Mac</strong>.&nbsp;</p>

					<p><strong>11. Sistema de chat propio</strong></p>

					<p>Aunque muchos jugadores siguen apostando por programas de terceros como Skype, TeamSpeak o Discord para comunicarse mientras juegan, Steam incluye&nbsp;<strong>servicio gratuito de chat de voz</strong>&nbsp;como de texto entre amigos para que puedas usarlo en tus juegos favoritos. No es necesario salir del juego en ningún momento para activar el chat o comunicarse.</p>


					<p><strong>12. Sistema para evitar a los tramposos (VAC)</strong></p>

					<p>Steam cuenta con un sistema de&nbsp;<strong>detección de programas externos de cheats&nbsp;</strong>entre su comunidad Steam Community que conlleva baneos para los jugadores que hagan trampas, intentando mantener de esta manera las partidas lo más limpias y justas posible.</p>

					<p><strong>13. Comparte juegos con tus amigos</strong></p>

					<p>Steam nos permite&nbsp;<strong>compartir nuestra cuenta con amigos</strong>, de tal forma que puedan acceder a nuestra biblioteca de juegos y jugar a los títulos que quieran mientras el jugador principal (nosotros) no se encuentre jugando al mismo tiempo.</p>

					<p><strong>14. Cromos e insignias</strong></p>

					<p>Cada vez más y más juegos en Steam incluyen cromos específicos. Al comprar un juego en Steam Store y jugarlo un cierto número de minutos iremos desbloqueando estas 'trading card' virtuales. Existe un&nbsp;<strong>mercado</strong>&nbsp;<strong>de cromos virtuales</strong>&nbsp;en el que negociar y comprar los cromos de cada juego que nos faltan; cuando los tengamos todos podemos crear una insignia y sumar puntos de experiencia en nuestra cuenta de Steam y exibirlas en nuestro perfil.&nbsp;</p>

					<p><strong>15. Compartir capturas y vídeos de tus juegos</strong></p>

					<p>Steam ofrece la posibilidad de&nbsp;<strong>compartir con la comunidad </strong>Steam Community capturas de imágenes y clips de vídeo de nuestros juegos favoritos con una herramienta interna. Estas capturas y vídeos de guardan en nuestro PC y las podemos usar también para compartirlas en redes sociales.</p>

					<p><strong>16. Sistema de logros</strong></p>

					<p><strong>&nbsp;</strong>Cada juego en Steam incluye una&nbsp;<strong>lista de logros</strong>&nbsp;personalizados e independientes que podemos desbloquear si cumplimos ciertos requisitos, dependiendo de cada juego.</p>

					<p><strong>17. Debatir sobre juegos en los foros con Steam Community</strong></p>

					<p>Cada juego en Steam incluye un punto de encuentro desde el que podemos acceder a un foro exclusivo donde debatir sobre cualquier tema con los demás jugadores. Allí podemos consultar nuestras dudas, ayudar a otros jugadores, avisar de errores o debatir sobre lo que nos gusta y lo que no.</p>

					<p><strong>18. Hacer regalos a amigos</strong></p>

					<p>En Steam no sólo puedes comprar juegos digitales en Steam Store para ti, si no&nbsp;<strong>regalárselos a alguno de tus amigos</strong>&nbsp;y así darles una sorpresa. Cuando adquieras un juego digital en Steam, tan sólo tienes que marcarlo como regalo y podrás enviarlo a cualquier parte del mundo.</p>

					<p><strong>19. Recomendaciones personalizadas y ofertas Steam</strong></p>

					<p>Con el paso de los años, Steam ha desarrollado un sistema de recomendaciones personalizadas para cada jugador basadas en sus intereses, juegos comprados anteriormente o marcados como favoritos. De esta manera es más fácil que nunca descubrir nuevos juegos que no conocíamos y que podrían gustarnos. Además, Steam ofrece periodos de&nbsp;<strong>rebajas al menos dos veces al año</strong>, cuando gran parte de su catálogo sufre reducciones muy importantes de precio.</p>

					<p><strong>20. Mentores de Steam</strong></p>

					<p>Steam incluye un programa de mentores (jugadores o medios influyentes) a los que cualquier jugador puede seguir. Estos mentores podrán sugerir a sus seguidores&nbsp;<strong>juegos recomendados&nbsp;</strong>justificando con sus comentarios por qué los recomiendan.</p>

